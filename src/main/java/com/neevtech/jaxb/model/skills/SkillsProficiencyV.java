package com.neevtech.jaxb.model.skills;

public class SkillsProficiencyV {
	private long proficiencyId;
	private String proficiencyCode;
	private String proficiency;
	
	public long getProficiencyId() {
		return proficiencyId;
	}
	public void setProficiencyId(long proficiencyId) {
		this.proficiencyId = proficiencyId;
	}
	public String getProficiencyCode() {
		return proficiencyCode;
	}
	public void setProficiencyCode(String proficiencyCode) {
		this.proficiencyCode = proficiencyCode;
	}
	public String getProficiency() {
		return proficiency;
	}
	public void setProficiency(String proficiency) {
		this.proficiency = proficiency;
	}
}
